#          ключевой компонент архитектуры Transformer, который позволяет модели
#          учитывать порядок токенов в последовательности.
#
#          **Основные возможности:**
#
#          1. **Синусоидальное кодирование**
#             - Использование синусов и косинусов разных частот
#             - Детерминистическое (не обучаемое) кодирование
#             - Позволяет модели обобщать на более длинные последовательности
#
#          2. **Формула позиционного кодирования**
#             - PE(pos, 2i) = sin(pos / 10000^(2i/d_model))
#             - PE(pos, 2i+1) = cos(pos / 10000^(2i/d_model))
#             - Где pos - позиция, i - размерность
#
#          3. **Преимущества синусоидального подхода**
#             - Каждая размерность кодирует разную частоту
#             - Линейные зависимости между позициями
#             - Модель может легко обучиться attend по относительным позициям
#
#          4. **Динамическое расширение**
#             - Автоматическое вычисление кодирования для длин > max_len
#             - Кэширование для ускорения
#             - Поддержка произвольной длины на этапе генерации
#
#          5. **Dropout регуляризация**
#             - Применяется после добавления позиционного кодирования
#             - Улучшает обобщение модели
#
# @usage
#     pos_encoding = PositionalEncoding(d_model=512, max_len=1024, dropout=0.1)
#     x = pos_encoding(x)    # x.shape = (batch, seq_len, d_model)
#
# @example
#     # Стандартное использование
#     pos_enc = PositionalEncoding(d_model=256, max_len=512)
#     embeddings = token_embedding(tokens)    # (batch, seq_len, 256)
#     embeddings = pos_enc(embeddings)        # Добавление позиционной информации
#
#     # Для очень длинных последовательностей (генерация)
#     pos_enc = PositionalEncoding(d_model=512, max_len=1024)
#     # При seq_len=2048 кодирование будет вычислено автоматически
#     long_sequence = pos_enc(long_embeddings)
#
# ======================================================================

import torch
import torch.nn as nn
import math

# ======================================================================
# КЛАСС PositionalEncoding
# ======================================================================

class PositionalEncoding(nn.Module):
    """
    Синусоидальное позиционное кодирование.
    
    Позволяет модели учитывать порядок токенов в последовательности.
    Использует синусы и косинусы разных частот.
    
    **Теоретическая справка:**
    
    В отличие от рекуррентных и сверточных сетей, Transformer не имеет
    встроенного механизма для учета порядка элементов. Positional Encoding
    добавляет информацию о позиции токена в последовательности.
    
    **Почему синусоидальное?**
    1. Детерминистическое - не требует обучения
    2. Позволяет модели обобщать на более длинные последовательности
    3. Линейные зависимости: PE(pos+k) может быть выражен как линейная функция PE(pos)
    4. Разные частоты для разных размерностей
    
    Args:
        d_model (int):   Размерность эмбеддингов
        max_len (int):   Максимальная длина последовательности (по умолчанию: 5000)
        dropout (float): Вероятность dropout (по умолчанию: 0.1)
    
    **Формула:**
        PE(pos, 2i) = sin(pos / 10000^(2i/d_model))
        PE(pos, 2i+1) = cos(pos / 10000^(2i/d_model))
        где:
        - pos - позиция токена в последовательности (0, 1, 2, ...)
        - i - индекс размерности эмбеддинга (0, 1, 2, ..., d_model/2 - 1)
        - d_model - общая размерность эмбеддинга

    **Визуализация частот:**
    - Младшие размерности (малые i):   Высокая частота (быстрые изменения)
    - Старшие размерности (большие i): Низкая частота (медленные изменения)

    @note
        Позиционное кодирование добавляется к эмбеддингам токенов (не конкатенируется)
    """

    def __init__(self, d_model: int, max_len: int = 5000, dropout: float = 0.1):
        super().__init__()
        self.dropout = nn.Dropout(p=dropout)
        self.d_model = d_model
        self.max_len = max_len
        
        # Предварительно вычисляем позиционные кодирования
        pe = torch.zeros(max_len, d_model)
        position = torch.arange(0, max_len, dtype=torch.float).unsqueeze(1)
        
        # Вычисляем делитель для разных частот
        # div_term = 1 / (10000^(2i/d_model))
        div_term = torch.exp(torch.arange(0, d_model, 2).float() * 
                            (-math.log(10000.0) / d_model))
        
        # Четные индексы: синус
        pe[:, 0::2] = torch.sin(position * div_term)
        # Нечетные индексы: косинус
        pe[:, 1::2] = torch.cos(position * div_term)
        
        # Регистрируем как буфер (не обучаемый параметр)
        # Добавляем batch dimension: (1, max_len, d_model)
        self.register_buffer('pe', pe.unsqueeze(0))

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Добавляет позиционное кодирование к входным эмбеддингам.
        
        Args:
            x (torch.Tensor): Входные эмбеддинги, форма (batch_size, seq_len, d_model)
        
        Returns:
            torch.Tensor: Эмбеддинги с добавленным позиционным кодированием и dropout,
                          форма (batch_size, seq_len, d_model)
        
        **Процесс:**
        1. Получение текущей длины последовательности
        2. Проверка, нужно ли расширить кэшированное кодирование
        3. Добавление позиционного кодирования к эмбеддингам
        4. Применение dropout для регуляризации
        
        **Динамическое расширение:**
        Если seq_len превышает изначальный max_len, кодирование для новых позиций
        вычисляется динамически и добавляется к кэшу для последующего использования.
        Это позволяет модели работать с последовательностями любой длины.
        
        @note
            Операция x + pe[:, :seq_len, :] - поэлементное сложение (broadcasting)
        """
        seq_len = x.size(1)
        
        # Если последовательность длиннее, чем предварительно вычисленная,
        # динамически расширяем позиционные кодирования
        if seq_len > self.pe.size(1):
            # Вычисляем дополнительные позиции
            additional_len = seq_len - self.pe.size(1)
            start_pos = self.pe.size(1)
            
            # Вычисляем дополнительные позиционные кодирования
            position = torch.arange(start_pos, seq_len, dtype=torch.float, device=x.device).unsqueeze(1)
            div_term = torch.exp(torch.arange(0, self.d_model, 2, device=x.device).float() * 
                                (-math.log(10000.0) / self.d_model))
            
            # Создаем дополнительные кодирования
            additional_pe = torch.zeros(additional_len, self.d_model, device=x.device)
            additional_pe[:, 0::2] = torch.sin(position * div_term)
            additional_pe[:, 1::2] = torch.cos(position * div_term)
            
            # Добавляем к буферу (конкатенируем)
            self.pe = torch.cat([self.pe, additional_pe.unsqueeze(0)], dim=1)
        
        # Добавляем позиционное кодирование и применяем dropout
        x = x + self.pe[:, :seq_len, :]
        return self.dropout(x)