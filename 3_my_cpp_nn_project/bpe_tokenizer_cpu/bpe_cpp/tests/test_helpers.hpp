#pragma once

#include <cstdio>
#include <filesystem>
#include <fstream>
#include <string>

namespace fs = std::filesystem;

namespace bpe_test {

// ============================================================================
// Операции с файлами для тестов
// ============================================================================

/**
 * @brief Безопасное удаление файла (игнорирует ошибки, если файл не существует)
 * 
 * @param path Путь к файлу для удаления
 * 
 * **Особенности:**
 * - Не выбрасывает исключений при отсутствии файла
 * - Использует std::error_code для игнорирования ошибок
 * - Идеален для очистки временных файлов в тестах
 * 
 * **Пример использования:**
 * @code
 * // Создаем временный файл
 * create_temp_file("test.txt", "content");
 * 
 * // Выполняем тест...
 * 
 * // Очищаем после теста (даже если файла нет - ошибки не будет)
 * safe_remove("test.txt");
 * @endcode
 */
inline void safe_remove(const std::string& path) {
    std::error_code ec;
    fs::remove(path, ec);
    // Игнорируем ошибки - файл может не существовать
}

/**
 * @brief Создать временный файл с указанным содержимым
 * 
 * @param path Путь для создания файла
 * @param content Содержимое файла
 * 
 * **Особенности:**
 * - Создает или перезаписывает файл по указанному пути
 * - Автоматически закрывает файл после записи
 * - Не проверяет успешность записи (для простоты тестов)
 * 
 * **Пример использования:**
 * @code
 * // Создаем тестовый JSON словарь
 * create_temp_file("vocab.json", R"(["a","b","c"])");
 * 
 * // Создаем тестовый файл слияний
 * create_temp_file("merges.txt", "#version: 0.1\na b\nb c");
 * @endcode
 * 
 * @warning Не проверяет ошибки записи - предполагается, что тестовая среда
 *          имеет права на запись в указанную директорию.
 */
inline void create_temp_file(const std::string& path, const std::string& content) {
    std::ofstream file(path);
    if (file.is_open()) {
        file << content;
        file.close();    // Явно закрываем, хотя деструктор сделает это автоматически
    }
}

/**
 * @brief Проверить существование файла
 * 
 * @param path Путь к файлу для проверки
 * @return true если файл существует и доступен
 * 
 * **Особенности:**
 * - Использует std::error_code для избегания исключений
 * - Работает одинаково на всех платформах
 * - Проверяет только существование, не права доступа
 * 
 * **Пример использования:**
 * @code
 * // Проверяем, что модель загружена
 * if (!file_exists("models/bpe_10000/cpp_vocab.json")) {
 *     GTEST_SKIP() << "Модель не найдена, пропускаем тест!";
 * }
 * @endcode
 */
inline bool file_exists(const std::string& path) {
    std::error_code ec;
    return fs::exists(path, ec);
}

}    // namespace bpe_test