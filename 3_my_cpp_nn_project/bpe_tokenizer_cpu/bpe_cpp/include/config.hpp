/**
 * @file config.hpp
 * @brief Пользовательские конфигурационные параметры BPE токенизатора
 * 
 * @author Евгений П.
 * @date 2026
 * @version 3.4.0
 * 
 * @details Этот файл содержит все настраиваемые параметры токенизатора.
 *          В отличие от генерируемого CMake config.h, эти параметры
 *          можно изменять вручную для тонкой настройки поведения.
 * 
 *          **Отличия от config.h:**
 *          - config.h   - Генерируется CMake (опции сборки, платформа)
 *          - config.hpp - Редактируется вручную (поведенческие настройки)
 * 
 *          **Категории настроек:**
 *          - Токенизация        - Базовые параметры алгоритма BPE
 *          - Кэширование        - Оптимизация повторяющихся вызовов
 *          - Память             - Управление аллокациями и пулами
 *          - Производительность - Параллелизация и батчинг
 *          - Обучение           - Параметры алгоритма обучения
 *          - Специальные токены - Конфигурация спецтокенов
 *          - Отладка            - Логирование и проверки
 *          - Экспорт            - Форматы сериализации
 * 
 * @note Все параметры - constexpr для максимальной производительности
 * @warning Изменение требует перекомпиляции проекта
 * 
 * @see BPETokenizer, FastBPETokenizer, config::Config
 */

#pragma once

#include <cstddef>
#include <cstdint>
#include <type_traits>

namespace bpe {
namespace config {

// ============================================================================
// Базовые параметры токенизации
// ============================================================================

/**
 * @name Параметры токенизации
 * Основные настройки, определяющие поведение алгоритма BPE
 * @{
 */

/**
 * @brief Использовать byte-level режим по умолчанию
 * 
 * **true**  - Обрабатывать UTF-8 как байты (поддержка всех символов)
 * **false** - Обрабатывать UTF-8 как символы (только для ASCII)
 * 
 * @note Включение рекомендуется для работы с многоязычными текстами
 */
constexpr bool DEFAULT_BYTE_LEVEL = true;

/**
 * @brief Максимальная длина токена в символах
 * 
 * Предотвращает создание чрезмерно длинных токенов в процессе обучения.
 * Токены длиннее этого значения не создаются.
 * 
 * @warning Слишком маленькое значение может ухудшить качество сжатия
 */
constexpr size_t DEFAULT_MAX_TOKEN_LENGTH = 1000;

/**
 * @brief Размер словаря по умолчанию
 * 
 * Количество токенов, создаваемых при обучении.
 * - Меньше 1000  - Грубая токенизация, мало памяти
 * - 8000-32000   - Оптимально для большинства задач
 * - Больше 50000 - Детальная токенизация, много памяти
 */
constexpr size_t DEFAULT_VOCAB_SIZE = 10000;

/**
 * @brief Минимальная частота символа для включения в словарь
 * 
 * Символы, встречающиеся реже этого порога, заменяются на <UNK>
 * Помогает отсеять опечатки и редкие символы.
 */
constexpr size_t MIN_CHAR_FREQUENCY = 2;

/**
 * @brief Максимальная длина входного текста (0 = без ограничений)
 * 
 * Тексты длиннее этого значения обрезаются.
 * Защита от слишком длинных входных последовательностей.
 */
constexpr size_t MAX_INPUT_LENGTH = 0;

/** @} */

// ============================================================================
// Настройки кэширования
// ============================================================================

/**
 * @name Оптимизация через кэширование
 * Параметры для кэширования результатов encode()
 * @{
 */

/**
 * @brief Включить кэширование результатов encode
 * 
 * Хранит результаты токенизации часто встречающихся текстов.
 * Дает прирост производительности 30-50% на повторяющихся данных.
 */
constexpr bool ENABLE_CACHE = true;

/**
 * @brief Максимальный размер кэша (количество записей)
 * 
 * - 1000   - Мало памяти, но низкий hit rate
 * - 10000  - Компромисс
 * - 100000 - Много памяти, высокий hit rate
 */
constexpr size_t CACHE_SIZE_LIMIT = 10000;

/**
 * @brief Коэффициент загрузки кэша (0.0 - 1.0)
 * 
 * При превышении этого порога старые записи удаляются.
 * 0.75 - хороший баланс между памятью и производительностью.
 */
constexpr double CACHE_LOAD_FACTOR = 0.75;

/**
 * @brief Время жизни записи в кэше (мс, 0 = бессрочно)
 * 
 * Автоматическая инвалидация устаревших записей.
 * Полезно для streaming-сценариев.
 */
constexpr uint64_t CACHE_TTL_MS = 0;

/**
 * @brief Стратегия вытеснения из кэша
 * 
 * - 0: LRU (Least Recently Used)   - Удаляем давно неиспользуемые
 * - 1: LFU (Least Frequently Used) - Удаляем редко используемые
 * - 2: FIFO (First In First Out)   - Удаляем самые старые
 */
constexpr int CACHE_EVICTION_POLICY = 0;

/** @} */

// ============================================================================
// Пути к файлам по умолчанию
// ============================================================================

/**
 * @name Пути к моделям
 * Стандартные расположения файлов моделей
 * @{
 */

/// Путь к файлу словаря (JSON)
constexpr const char* DEFAULT_VOCAB_PATH = "models/bpe_10000/cpp_vocab.json";

/// Путь к файлу слияний (TXT)
constexpr const char* DEFAULT_MERGES_PATH = "models/bpe_10000/cpp_merges.txt";

/// Путь к бинарной модели (быстрая загрузка)
constexpr const char* DEFAULT_BINARY_MODEL_PATH = "models/bpe_10000/model.bin";

/// Путь для экспорта в HuggingFace формат
constexpr const char* DEFAULT_HF_EXPORT_PATH = "models/hf_model/";

/// Путь для сохранения чекпоинтов при обучении
constexpr const char* DEFAULT_CHECKPOINT_PATH = "checkpoints/";

/** @} */

// ============================================================================
// Настройки производительности
// ============================================================================

/**
 * @name Оптимизация производительности
 * Параметры для ускорения обработки
 * @{
 */

/**
 * @brief Размер пакета при пакетной обработке
 * 
 * Оптимальный размер батча для encode_batch().
 * Слишком маленький - Много накладных расходов
 * Слишком большой:  - Неравномерное время обработки
 */
constexpr size_t ENCODE_BATCH_SIZE = 32;

/**
 * @brief Использовать параллельное кодирование
 * 
 * Распределяет encode_batch() между потоками.
 * Требует поддержки OpenMP или TBB.
 */
constexpr bool USE_PARALLEL_ENCODE = true;

/**
 * @brief Минимальный размер текста для параллельной обработки (байты)
 * 
 * Тексты меньше этого размера обрабатываются последовательно,
 * чтобы избежать накладных расходов на создание потоков.
 */
constexpr size_t PARALLEL_ENCODE_THRESHOLD = 1024;

/**
 * @brief Количество потоков по умолчанию (0 = auto)
 * 
 * - 0 - Использовать все доступные ядра
 * - N - Использовать ровно N потоков
 */
constexpr int DEFAULT_NUM_THREADS = 0;

/**
 * @brief Использовать пул потоков
 * 
 * Потоки создаются один раз и переиспользуются.
 * Уменьшает накладные расходы на создание/уничтожение.
 */
constexpr bool USE_THREAD_POOL = true;

/**
 * @brief Размер пула потоков (0 = auto)
 */
constexpr size_t THREAD_POOL_SIZE = 0;

/** @} */

// ============================================================================
// Настройки пула памяти
// ============================================================================

/**
 * @name Оптимизация памяти
 * Параметры для эффективного управления памятью
 * @{
 */

/**
 * @brief Использовать пул памяти
 * 
 * Значительно уменьшает количество вызовов new/delete
 * и улучшает локальность данных.
 */
constexpr bool USE_MEMORY_POOL = true;

/**
 * @brief Начальный размер пула (байты)
 * 
 * Выделяется сразу при создании пула.
 * 1 МБ - Хороший стартовый размер.
 */
constexpr size_t INITIAL_POOL_SIZE = 1024 * 1024;

/**
 * @brief Максимальный размер пула (байты, 0 = без ограничений)
 * 
 * Пул может расти до этого размера.
 * 64 МБ - Разумный лимит для большинства задач.
 */
constexpr size_t MAX_POOL_SIZE = 64 * 1024 * 1024;

/**
 * @brief Размер блока расширения пула (байты)
 * 
 * При нехватке памяти пул расширяется блоками этого размера.
 * 256 КБ - Хороший баланс.
 */
constexpr size_t POOL_GROWTH_BLOCK = 256 * 1024;

/**
 * @brief Использовать выравнивание для кэш-линий
 * 
 * Предотвращает false sharing в многопоточных сценариях.
 * Важно для высоконагруженных систем.
 */
constexpr bool USE_CACHE_ALIGNMENT = true;

/**
 * @brief Размер кэш-линии (байты)
 * 
 * Обычно 64 на x86, 128 на некоторых ARM.
 */
constexpr size_t CACHE_LINE_SIZE = 64;

/** @} */

// ============================================================================
// Настройки обучения
// ============================================================================

/**
 * @name Параметры обучения
 * Настройки алгоритма обучения BPE
 * @{
 */

/**
 * @brief Размер чанка для параллельной обработки
 * 
 * Корпус разбивается на чанки этого размера для распределения
 * между потоками при обучении.
 */
constexpr size_t TRAIN_CHUNK_SIZE = 1000;

/**
 * @brief Сохранять чекпоинты в процессе обучения
 * 
 * Позволяет возобновить обучение с места остановки.
 */
constexpr bool SAVE_CHECKPOINTS = false;

/**
 * @brief Частота сохранения чекпоинтов (шаги)
 * 
 * Сохранять модель каждые N слияний.
 */
constexpr size_t CHECKPOINT_FREQUENCY = 100;

/**
 * @brief Максимальное количество итераций обучения
 * 
 * Защита от бесконечного цикла.
 */
constexpr size_t MAX_TRAIN_ITERATIONS = 100000;

/**
 * @brief Минимальная частота пары для слияния
 * 
 * Пары с частотой ниже этого порога игнорируются.
 * Отсеивает редкие комбинации.
 */
constexpr size_t MIN_PAIR_FREQUENCY = 2;

/**
 * @brief Показывать прогресс обучения
 * 
 * Выводит текущий шаг и информацию о слиянии.
 */
constexpr bool SHOW_TRAIN_PROGRESS = true;

/** @} */

// ============================================================================
// Специальные токены
// ============================================================================

/**
 * @name Специальные токены
 * Токены с особым значением в модели
 * @{
 */

/// Неизвестный токен (для символов вне словаря)
constexpr const char* UNKNOWN_TOKEN = "<UNK>";

/// Токен паддинга (выравнивание последовательностей)
constexpr const char* PAD_TOKEN = "<PAD>";

/// Токен начала последовательности
constexpr const char* BOS_TOKEN = "<BOS>";

/// Токен конца последовательности
constexpr const char* EOS_TOKEN = "<EOS>";

/// Токен маски (для masked language modeling)
constexpr const char* MASK_TOKEN = "<MASK>";

/// Токен разделителя предложений
constexpr const char* SEP_TOKEN = "<SEP>";

/// Токен классификации (для BERT-like моделей)
constexpr const char* CLS_TOKEN = "<CLS>";

/** @} */

// ============================================================================
// Настройки отладки
// ============================================================================

/**
 * @name Режимы отладки
 * Параметры для разработки и отладки
 * @{
 */

/**
 * @brief Включить детальное логирование
 * 
 * Выводит отладочную информацию в std::cerr.
 * Отключать для продакшена.
 */
constexpr bool ENABLE_DEBUG_LOGGING = false;

/**
 * @brief Проверять границы массивов
 * 
 * Дополнительные проверки индексов.
 * Помогает отловить ошибки, но замедляет выполнение.
 */
constexpr bool ENABLE_BOUNDS_CHECKS = false;

/**
 * @brief Профилировать производительность
 * 
 * Собирает статистику времени выполнения операций.
 * Использовать только для бенчмарков.
 */
constexpr bool ENABLE_PROFILING = false;

/**
 * @brief Уровень детализации логов
 * 
 * - 0 - Только ошибки
 * - 1 - Добавочные предупреждения
 * - 2 - Добавочная информация
 * - 3 - Отладочные сообщения
 */
constexpr int LOG_LEVEL = 1;

/**
 * @brief Проверять целостность модели при загрузке
 * 
 * Вычисляет и проверяет контрольную сумму.
 */
constexpr bool VALIDATE_MODEL_ON_LOAD = true;

/**
 * @brief Сохранять статистику операций
 * 
 * Счетчики вызовов encode/decode и т.д.
 */
constexpr bool COLLECT_STATISTICS = false;

/** @} */

// ============================================================================
// Настройки экспорта/импорта
// ============================================================================

/**
 * @name Сериализация
 * Параметры сохранения и загрузки моделей
 * @{
 */

/**
 * @brief Формат экспорта по умолчанию
 * 
 * - 0 - JSON (два файла: vocab.json + merges.txt)
 * - 1 - JSON (единый файл)
 * - 2 - Бинарный (.bin)
 * - 3 - HuggingFace (совместимый формат)
 */
constexpr int DEFAULT_EXPORT_FORMAT = 0;

/**
 * @brief Сжимать JSON вывод (убирать пробелы)
 * 
 * Уменьшает размер файла, но теряется читаемость.
 */
constexpr bool COMPRESS_JSON = false;

/**
 * @brief Добавлять метаданные в модель
 * 
 * Сохраняет версию, дату создания и другую информацию.
 */
constexpr bool INCLUDE_METADATA = true;

/**
 * @brief Проверять контрольную сумму при загрузке
 * 
 * Гарантирует целостность загружаемой модели.
 */
constexpr bool VERIFY_CHECKSUM = true;

/** @} */

// ============================================================================
// Валидация параметров (compile-time проверки)
// ============================================================================

// Проверки кэширования
static_assert(CACHE_SIZE_LIMIT > 0, "Размер кэша должен быть положительным");
static_assert(CACHE_LOAD_FACTOR > 0.0 && CACHE_LOAD_FACTOR <= 1.0,
              "Коэффициент загрузки кэша должен быть в интервале (0, 1]");
static_assert(CACHE_EVICTION_POLICY >= 0 && CACHE_EVICTION_POLICY <= 2,
              "Политика вытеснения кэша должна быть 0, 1 или 2");

// Проверки памяти
static_assert(DEFAULT_MAX_TOKEN_LENGTH > 0, "Максимальная длина токена должна быть положительной!");
static_assert(INITIAL_POOL_SIZE > 0, "Начальный размер пула должен быть положительным!");
static_assert(MAX_POOL_SIZE >= INITIAL_POOL_SIZE || MAX_POOL_SIZE == 0, 
              "Максимальный размер пула должен быть >= начальный размер пула или 0!");
static_assert(POOL_GROWTH_BLOCK > 0, "Размер блока роста пула должен быть положительным!");
static_assert(CACHE_LINE_SIZE == 64 || CACHE_LINE_SIZE == 128,
              "Размер кэш-линии должен быть 64 или 128 байт!");

// Проверки обучения
static_assert(TRAIN_CHUNK_SIZE > 0, "Размер train chunk должен быть положительным!");
static_assert(MAX_TRAIN_ITERATIONS > 0, "Максимальное количество итераций должно быть положительным!");
static_assert(MIN_PAIR_FREQUENCY >= 1, "Минимальная частота пары должна быть >= 1!");

// Проверки пакетной обработки
static_assert(ENCODE_BATCH_SIZE > 0, "Размер encode batch должен быть положительным!");
static_assert(PARALLEL_ENCODE_THRESHOLD >= 0, "Порог параллельной обработки не может быть отрицательным!");

// Проверки отладки
static_assert(LOG_LEVEL >= 0 && LOG_LEVEL <= 3,
              "Уровень логирования должен быть от 0 до 3!");

// Проверки экспорта
static_assert(DEFAULT_EXPORT_FORMAT >= 0 && DEFAULT_EXPORT_FORMAT <= 3,
              "Формат экспорта должен быть от 0 до 3!");

/** @} */    // Конец namespace config

}    // namespace config
}    // namespace bpe

/**
 * @example examples/config_demo.cpp
 * Демонстрация использования конфигурации:
 * 
 * @include examples/config_demo.cpp
 * 
 * Пример кода:
 * @code
 * #include "config.hpp"
 * #include "fast_tokenizer.hpp"
 * #include <iostream>
 * 
 * void print_config() {
 *     using namespace bpe::config;
 *     
 *     std::cout << "============================================================\n";
 *     std::cout << " КОНФИГУРАЦИЯ ТОКЕНИЗАТОРА\n";
 *     std::cout << "============================================================\n\n";
 *     
 *     std::cout << "Токенизация:\n";
 *     std::cout << "- byte-level:     " << (DEFAULT_BYTE_LEVEL ? "да" : "нет") << "\n";
 *     std::cout << "- макс. токен:    " << DEFAULT_MAX_TOKEN_LENGTH << "\n";
 *     std::cout << "- размер словаря: " << DEFAULT_VOCAB_SIZE << "\n\n";
 *     
 *     std::cout << "Производительность:\n";
 *     std::cout << "- пул памяти:     " << (USE_MEMORY_POOL ? "да" : "нет") << "\n";
 *     std::cout << "- размер пула:    " << INITIAL_POOL_SIZE / 1024 << " КБ\n";
 *     std::cout << "- кэш:            " << CACHE_SIZE_LIMIT << " записей\n";
 * }
 * @endcode
 */