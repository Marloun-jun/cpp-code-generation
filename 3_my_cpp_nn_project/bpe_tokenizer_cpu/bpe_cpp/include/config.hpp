/**
 * @file config.hpp
 * @brief Конфигурационные параметры и настройки BPE токенизатора
 * 
 * @author Евгений П.
 * @date 2026
 * @version 3.4.0
 * 
 * @details Этот файл содержит глобальные конфигурационные параметры,
 *          управляющие поведением токенизатора. В отличие от config.h,
 *          который генерируется CMake, этот файл содержит пользовательские
 *          настройки, которые можно изменять вручную.
 * 
 *          Настройки разделены на категории:
 *          - **Параметры токенизации**    - базовые настройки алгоритма
 *          - **Кэширование**              - оптимизация повторяющихся операций
 *          - **Пути к моделям**           - расположение файлов по умолчанию
 *          - **Производительность**       - настройки для оптимизации скорости
 *          - **Пул памяти**               - управление выделением памяти
 *          - **Обучение**                  - параметры для алгоритма обучения
 *          - **Специальные токены**         - токены с особым значением
 *          - **Отладка**                   - режимы для разработчиков
 * 
 * @note Все параметры являются constexpr для максимальной производительности
 * @warning Изменение параметров требует перекомпиляции проекта
 * 
 * @see FastBPETokenizer
 * @see BPETokenizer
 * @see TokenizerConfig
 */

#pragma once

#include <cstddef>
#include <cstdint>
#include <type_traits>

namespace bpe {
namespace config {

// ======================================================================
// Параметры токенизации по умолчанию
// ======================================================================

/**
 * @name Параметры токенизации по умолчанию
 * Базовые настройки, определяющие поведение токенизатора
 */
/**@{*/

/**
 * @brief Использовать byte-level режим по умолчанию
 * 
 * Byte-level режим обрабатывает UTF-8 текст на уровне байтов,
 * что гарантирует корректную работу с любыми символами Unicode,
 * но увеличивает длину последовательностей.
 * 
 * Рекомендуется: true для поддержки Unicode, false для ASCII-only текстов
 */
constexpr bool DEFAULT_BYTE_LEVEL = true;

/**
 * @brief Максимальная длина токена в символах
 * 
 * Ограничивает рост токенов при слияниях. Токены длиннее этого значения
 * не будут создаваться, что предотвращает бесконечный рост.
 * 
 * Рекомендуется:    1000-5000 в зависимости от языка
 */
constexpr size_t DEFAULT_MAX_TOKEN_LENGTH = 1000;

/**
 * @brief Размер словаря по умолчанию для обучения
 * 
 * Определяет, сколько токенов будет создано при обучении.
 * Больший словарь = лучшее сжатие, но больше памяти и медленнее.
 */
constexpr size_t DEFAULT_VOCAB_SIZE = 8000;

/**
 * @brief Минимальная частота символа для включения в словарь
 * 
 * Символы, встречающиеся реже этого порога, будут заменены на <UNK>
 */
constexpr size_t MIN_CHAR_FREQUENCY = 2;

/**
 * @brief Максимальная длина входного текста
 * 
 * Тексты длиннее этого значения будут обрезаны.
 * 0 = без ограничений.
 */
constexpr size_t MAX_INPUT_LENGTH = 0;  // без ограничений

/**@}*/

// ======================================================================
// Настройки кэширования
// ======================================================================

/**
 * @name Настройки кэширования
 * Параметры для оптимизации через кэширование результатов encode
 */
/**@{*/

/**
 * @brief Включить кэширование результатов encode
 * 
 * Кэш хранит результаты токенизации для часто встречающихся текстов.
 * Значительно ускоряет повторяющиеся запросы.
 */
constexpr bool ENABLE_CACHE = true;

/**
 * @brief Максимальный размер кэша (количество записей)
 * 
 * Больший кэш = выше hit rate, но больше потребление памяти.
 * Рекомендуется: 10000-100000 в зависимости от доступной памяти
 */
constexpr size_t CACHE_SIZE_LIMIT = 10000;

/**
 * @brief Коэффициент загрузки кэша (0.0 - 1.0)
 * 
 * При превышении этого значения старые записи удаляются.
 * Меньшие значения = более агрессивная очистка.
 */
constexpr double CACHE_LOAD_FACTOR = 0.75;

/**
 * @brief Время жизни записи в кэше (в миллисекундах)
 * 
 * 0 = бессрочно. Положительное значение = автоматическая инвалидация
 * устаревших записей.
 */
constexpr uint64_t CACHE_TTL_MS = 0;

/**
 * @brief Стратегия вытеснения из кэша
 * 0 = LRU (Least Recently Used)
 * 1 = LFU (Least Frequently Used)
 * 2 = FIFO (First In First Out)
 */
constexpr int CACHE_EVICTION_POLICY = 0;    // LRU

/**@}*/

// ======================================================================
// Пути к файлам моделей
// ======================================================================

/**
 * @name Пути к файлам моделей
 * Стандартные пути для сохранения и загрузки моделей
 * 
 * @note Пути указаны относительно корня проекта
 * @note Эти пути используются, если не указаны явно
 */
/**@{*/

/**
 * @brief Путь к файлу словаря по умолчанию
 * 
 * Формат: JSON с отображением токен -> ID
 */
constexpr const char* DEFAULT_VOCAB_PATH = "models/bpe_8000/cpp_vocab.json";

/**
 * @brief Путь к файлу слияний по умолчанию
 * 
 * Формат: текстовый файл с парами для слияния (по одной паре на строку)
 */
constexpr const char* DEFAULT_MERGES_PATH = "models/bpe_8000/cpp_merges.txt";

/**
 * @brief Путь для сохранения модели в бинарном формате
 */
constexpr const char* DEFAULT_BINARY_MODEL_PATH = "models/bpe_8000/model.bin";

/**
 * @brief Путь к директории для экспорта в HuggingFace формат
 */
constexpr const char* DEFAULT_HF_EXPORT_PATH = "models/hf_model/";

/**
 * @brief Путь к директории для чекпоинтов при обучении
 */
constexpr const char* DEFAULT_CHECKPOINT_PATH = "checkpoints/";

/**@}*/

// ======================================================================
// Настройки производительности
// ======================================================================

/**
 * @name Настройки производительности
 * Параметры для оптимизации скорости обработки
 */
/**@{*/

/**
 * @brief Размер пакета при пакетной обработке
 * 
 * Оптимальный размер батча для encode_batch.
 * Слишком маленький = много накладных расходов
 * Слишком большой = неравномерное время обработки
 */
constexpr size_t ENCODE_BATCH_SIZE = 32;

/**
 * @brief Использовать параллельное кодирование
 * 
 * Если true, encode_batch будет распределять работу между потоками.
 * Требует поддержки OpenMP или TBB.
 */
constexpr bool USE_PARALLEL_ENCODE = true;

/**
 * @brief Минимальный размер текста для параллельной обработки
 * 
 * Тексты меньше этого размера обрабатываются последовательно,
 * чтобы избежать накладных расходов на создание потоков.
 */
constexpr size_t PARALLEL_ENCODE_THRESHOLD = 1024;    // 1 КБ

/**
 * @brief Количество потоков по умолчанию
 * 
 * 0 = использовать все доступные ядра
 */
constexpr int DEFAULT_NUM_THREADS = 0;

/**
 * @brief Использовать пул потоков
 * 
 * Если true, потоки создаются один раз и переиспользуются.
 * Уменьшает накладные расходы на создание/уничтожение потоков.
 */
constexpr bool USE_THREAD_POOL = true;

/**
 * @brief Размер пула потоков (0 = auto)
 */
constexpr size_t THREAD_POOL_SIZE = 0;

/**@}*/

// ======================================================================
// Настройки пула памяти
// ======================================================================

/**
 * @name Настройки пула памяти
 * Параметры для оптимизации выделения памяти
 */
/**@{*/

/**
 * @brief Использовать пул памяти для частых аллокаций
 * 
 * Пул памяти значительно уменьшает количество вызовов new/delete
 * и улучшает локальность данных.
 */
constexpr bool USE_MEMORY_POOL = true;

/**
 * @brief Начальный размер пула памяти (в байтах)
 * 
 * Пул памяти используется для частых аллокаций при кодировании.
 * Больший размер уменьшает количество выделений памяти, но увеличивает
 * потребление памяти с самого старта.
 */
constexpr size_t INITIAL_POOL_SIZE = 1024 * 1024;    // 1 МБ

/**
 * @brief Максимальный размер пула памяти (в байтах)
 * 
 * Пул может расти до этого размера при необходимости.
 * Установите 0 для неограниченного роста.
 */
constexpr size_t MAX_POOL_SIZE = 64 * 1024 * 1024;    // 64 МБ

/**
 * @brief Размер блока при расширении пула (в байтах)
 */
constexpr size_t POOL_GROWTH_BLOCK = 256 * 1024;    // 256 КБ

/**
 * @brief Использовать выравнивание для кэш-линий
 * 
 * Предотвращает false sharing в многопоточных сценариях.
 */
constexpr bool USE_CACHE_ALIGNMENT = true;

/**
 * @brief Размер кэш-линии в байтах
 */
constexpr size_t CACHE_LINE_SIZE = 64;

/**@}*/

// ======================================================================
// Настройки обучения
// ======================================================================

/**
 * @name Настройки обучения
 * Параметры для алгоритма BPE обучения
 */
/**@{*/

/**
 * @brief Размер чанка для параллельной обработки при обучении
 * 
 * Корпус разбивается на чанки этого размера для распределения
 * между потоками.
 */
constexpr size_t TRAIN_CHUNK_SIZE = 1000;

/**
 * @brief Сохранять промежуточные результаты обучения
 * 
 * Если true, после каждого N слияний сохраняется чекпоинт
 */
constexpr bool SAVE_CHECKPOINTS = false;

/**
 * @brief Частота сохранения чекпоинтов (количество слияний)
 */
constexpr size_t CHECKPOINT_FREQUENCY = 100;

/**
 * @brief Максимальное количество итераций обучения
 * 
 * Предохранитель от бесконечного цикла.
 */
constexpr size_t MAX_TRAIN_ITERATIONS = 100000;

/**
 * @brief Минимальная частота пары для слияния
 * 
 * Пары с частотой ниже этого значения игнорируются.
 */
constexpr size_t MIN_PAIR_FREQUENCY = 2;

/**
 * @brief Показывать прогресс обучения
 */
constexpr bool SHOW_TRAIN_PROGRESS = true;

/**@}*/

// ======================================================================
// Настройки специальных токенов
// ======================================================================

/**
 * @name Специальные токены
 * Токены с особым значением в модели
 */
/**@{*/

/**
 * @brief Токен для неизвестных символов
 */
constexpr const char* UNKNOWN_TOKEN = "<UNK>";

/**
 * @brief Токен для паддинга
 */
constexpr const char* PAD_TOKEN = "<PAD>";

/**
 * @brief Токен начала последовательности
 */
constexpr const char* BOS_TOKEN = "<BOS>";

/**
 * @brief Токен конца последовательности
 */
constexpr const char* EOS_TOKEN = "<EOS>";

/**
 * @brief Токен маски (для masked language modeling)
 */
constexpr const char* MASK_TOKEN = "<MASK>";

/**
 * @brief Токен разделителя предложений
 */
constexpr const char* SEP_TOKEN = "<SEP>";

/**
 * @brief Токен классификации
 */
constexpr const char* CLS_TOKEN = "<CLS>";

/**@}*/

// ======================================================================
// Режимы отладки
// ======================================================================

/**
 * @name Режимы отладки
 * Включают дополнительные проверки и логирование
 */
/**@{*/

/**
 * @brief Включить детальное логирование
 * 
 * Если true, токенизатор будет выводить отладочную информацию
 * в std::cerr.
 */
constexpr bool ENABLE_DEBUG_LOGGING = false;

/**
 * @brief Проверять границы массивов
 * 
 * Включает дополнительные проверки индексов в критических местах.
 * Рекомендуется для отладки, отключать для продакшена.
 */
constexpr bool ENABLE_BOUNDS_CHECKS = false;

/**
 * @brief Профилировать производительность
 * 
 * Собирать статистику времени выполнения операций.
 * Влияет на производительность, использовать только для бенчмарков.
 */
constexpr bool ENABLE_PROFILING = false;

/**
 * @brief Уровень детализации логов (0-3)
 * 
 * 0 = только ошибки
 * 1 = + предупреждения
 * 2 = + информация
 * 3 = + отладочные сообщения
 */
constexpr int LOG_LEVEL = 1;

/**
 * @brief Проверять целостность модели при загрузке
 */
constexpr bool VALIDATE_MODEL_ON_LOAD = true;

/**
 * @brief Сохранять статистику операций
 */
constexpr bool COLLECT_STATISTICS = false;

/**@}*/

// ======================================================================
// Настройки экспорта/импорта
// ======================================================================

/**
 * @name Настройки экспорта/импорта
 * Параметры для сериализации моделей
 */
/**@{*/

/**
 * @brief Формат экспорта по умолчанию
 * 0 = JSON (два файла)
 * 1 = JSON (единый файл)
 * 2 = бинарный
 * 3 = HuggingFace
 */
constexpr int DEFAULT_EXPORT_FORMAT = 0;

/**
 * @brief Сжимать JSON вывод
 */
constexpr bool COMPRESS_JSON = false;

/**
 * @brief Добавлять метаданные в сохраненную модель
 */
constexpr bool INCLUDE_METADATA = true;

/**
 * @brief Проверять контрольную сумму при загрузке
 */
constexpr bool VERIFY_CHECKSUM = true;

/**@}*/

// ======================================================================
// Проверки и статические утверждения
// ======================================================================

// Проверка корректности значений
static_assert(CACHE_SIZE_LIMIT > 0, "Размер кэша должен быть положительным");
static_assert(DEFAULT_MAX_TOKEN_LENGTH > 0, "Максимальная длина токена должна быть положительной");
static_assert(CACHE_LOAD_FACTOR > 0.0 && CACHE_LOAD_FACTOR <= 1.0,
              "Коэффициент загрузки кэша должен быть в интервале (0, 1]");
static_assert(INITIAL_POOL_SIZE > 0, "Начальный размер пула должен быть положительным");
static_assert(MAX_POOL_SIZE >= INITIAL_POOL_SIZE || MAX_POOL_SIZE == 0, 
              "Максимальный размер пула должен быть >= начальный размер пула или 0");
static_assert(POOL_GROWTH_BLOCK > 0, "Размер блока роста пула должен быть положительным");

// Проверки для обучения
static_assert(TRAIN_CHUNK_SIZE > 0, "Размер train chunk должен быть положительным");
static_assert(MAX_TRAIN_ITERATIONS > 0, "Максимальное количество итераций должно быть положительным");
static_assert(MIN_PAIR_FREQUENCY >= 1, "Минимальная частота пары должна быть >= 1");

// Проверки для пакетной обработки
static_assert(ENCODE_BATCH_SIZE > 0, "Размер encode batch должен быть положительным");
static_assert(PARALLEL_ENCODE_THRESHOLD >= 0, "Порог параллельной обработки не может быть отрицательным");

// Проверки для политики кэширования
static_assert(CACHE_EVICTION_POLICY >= 0 && CACHE_EVICTION_POLICY <= 2,
              "Политика вытеснения кэша должна быть 0, 1 или 2");

// Проверка размера кэш-линии
static_assert(CACHE_LINE_SIZE == 64 || CACHE_LINE_SIZE == 128,
              "Размер кэш-линии должен быть 64 или 128 байт");

// Проверка уровня логирования
static_assert(LOG_LEVEL >= 0 && LOG_LEVEL <= 3,
              "Уровень логирования должен быть от 0 до 3");

} // namespace config
} // namespace bpe

/**
 * @example examples/config_example.cpp
 * Пример использования конфигурации:
 * 
 * @code{.cpp}
 * #include "config.hpp"
 * #include "fast_tokenizer.hpp"
 * #include <iostream>
 * 
 * int main() {
 *     using namespace bpe::config;
 *     
 *     std::cout << "╔════════════════════════════════════════╗\n";
 *     std::cout << "║     КОНФИГУРАЦИЯ ТОКЕНИЗАТОРА         ║\n";
 *     std::cout << "╚════════════════════════════════════════╝\n\n";
 *     
 *     std::cout << "📊 Параметры токенизации:\n";
 *     std::cout << "  • byte-level:            " << (DEFAULT_BYTE_LEVEL ? "да" : "нет") << "\n";
 *     std::cout << "  • макс. длина токена:    " << DEFAULT_MAX_TOKEN_LENGTH << "\n";
 *     std::cout << "  • размер словаря:        " << DEFAULT_VOCAB_SIZE << "\n";
 *     std::cout << "  • мин. частота символа:  " << MIN_CHAR_FREQUENCY << "\n\n";
 *     
 *     std::cout << "💾 Кэширование:\n";
 *     std::cout << "  • кэш:                   " << (ENABLE_CACHE ? "вкл" : "выкл") << "\n";
 *     std::cout << "  • размер кэша:           " << CACHE_SIZE_LIMIT << " записей\n";
 *     std::cout << "  • политика вытеснения:   " 
 *               << (CACHE_EVICTION_POLICY == 0 ? "LRU" :
 *                   CACHE_EVICTION_POLICY == 1 ? "LFU" : "FIFO") << "\n\n";
 *     
 *     std::cout << "⚡ Производительность:\n";
 *     std::cout << "  • пул памяти:            " << (USE_MEMORY_POOL ? "вкл" : "выкл") << "\n";
 *     std::cout << "  • начальный размер пула: " << INITIAL_POOL_SIZE / 1024 << " КБ\n";
 *     std::cout << "  • параллельный encode:   " << (USE_PARALLEL_ENCODE ? "да" : "нет") << "\n";
 *     std::cout << "  • потоков:               " << (DEFAULT_NUM_THREADS ? DEFAULT_NUM_THREADS : "auto") << "\n\n";
 *     
 *     std::cout << "🔧 Отладка:\n";
 *     std::cout << "  • режим отладки:         " << (ENABLE_DEBUG_LOGGING ? "вкл" : "выкл") << "\n";
 *     std::cout << "  • профилирование:        " << (ENABLE_PROFILING ? "вкл" : "выкл") << "\n";
 *     std::cout << "  • уровень логирования:   " << LOG_LEVEL << "\n\n";
 *     
 *     // Создание токенизатора с пользовательскими настройками
 *     struct TokenizerConfig {
 *         bool byte_level = DEFAULT_BYTE_LEVEL;
 *         size_t cache_size = CACHE_SIZE_LIMIT;
 *         bool use_memory_pool = USE_MEMORY_POOL;
 *         int num_threads = DEFAULT_NUM_THREADS;
 *         bool enable_profiling = ENABLE_PROFILING;
 *     };
 *     
 *     TokenizerConfig cfg;
 *     std::cout << "✅ Конфигурация загружена\n";
 *     
 *     return 0;
 * }
 * @endcode
 */